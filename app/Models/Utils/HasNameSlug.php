<?php

declare(strict_types=1);

namespace App\Models\Utils;

trait HasNameSlug
{

    /**
     * @var string
     */
    private string $name;
    /**
     * @var string
     */
    private string $slug;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @return string
     */
    public function getSlug(): string
    {
        return $this->slug;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }

    /**
     * @param string $slug
     */
    public function setSlug(string $slug): void
    {
        $this->slug = $slug;
    }
}
